package game;

/**
 *
 * @author leonardo
 */
public class Profile {

    private Level level;
    private Player player;

    public Profile(Level level, Player player) {
        this.level = level;
        this.player = player;
    }

    public Level getLevel() {
        return level;
    }

    public void setLevel(Level level) {
        this.level = level;
    }

    public Player getPlayer() {
        return player;
    }

    public void setPlayer(Player player) {
        this.player = player;
    }

}
